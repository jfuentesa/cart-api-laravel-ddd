<?php

namespace App\CartAPI\Infrastructure\Repository;

class RepositoryConfiguration
{
    public $path;

    /**
     * Get the value of path
     */ 
    public function getPath()
    {
        return $this->path;
    }

    /**
     * Set the value of path
     *
     * @return  self
     */ 
    public function setPath($path)
    {
        $this->path = $path;

        return $this;
    }
}

